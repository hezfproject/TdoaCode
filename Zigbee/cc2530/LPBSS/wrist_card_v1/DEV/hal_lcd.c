/**************************************************************************************************
 * @fn      HalLcd_HW_Control
 *
 * @brief   Write 1 command to the LCD
 *
 * @param   uint8 cmd - command to be written to the LCD
 *
 * @return  None
 **************************************************************************************************/
void HalLcd_HW_Control(uint8 cmd)
{
  UINT8 cmdData1 = 0;
  UINT8 cmdData2 = 0;
  LCD_SPI_BEGIN();
  LCD_DO_CONTROL();
  cmdData1 |= cmd>>1;
  if(cmd&0x01)cmdData2=0x80;
  LCD_SPI_TX(cmdData1);
  LCD_SPI_TX(cmdData2);
  LCD_SPI_WAIT_RXRDY();
  LCD_SPI_END();
}

