void LCD_IDPrint(UINT16 id)
{
    uint8 num = 0;
	uint8 offset = 0;
	uint16 displayNum = 0;

	HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[10],64);
	offset += 16;
	HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[11],64);
	offset += 16;
	HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[12],64);
	offset += 16;
	for(uint8 idx = 0; idx <5;idx++)
	{
		uint8 sIdx = idx;
		uint16 model = 1;
		while(sIdx < 4)
		{
			model *= 10;
			sIdx++;
		}

		if(id/model)
		{
			num = idx;
			break;
		}

	}

	switch(num)
	{
	   case 0:
		//; *10000
		displayNum = id/10000;
		HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[displayNum],64);
		offset += 16;
	   case 1:
		//; *1000
		displayNum = (id%10000)/1000;
		HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[displayNum],64);
		offset += 16;

	   case 2:
		//; *100
		displayNum = (id%1000)/100;
		HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[displayNum],64);
		offset += 16;
	   case 3:
		//; *10
		displayNum = (id%100)/10;
		HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[displayNum],64);
		offset += 16;
	   case 4:
		//; *1
		displayNum = id%10;
		HalLcd_BigChar_StartX(offset,3,4,DisplayNum32X16[displayNum],64);

		default:
			break;
	}



}


