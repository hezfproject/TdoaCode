uint8 HalFillVoiceBellbuf(void)
{
    halIntState_t   intState;
    uint16 maxBufSize = voicedqueue_getmaxsize(&writectrl.bufctrl);
    uint16 datasize = voicedqueue_getsize(&writectrl.bufctrl);

    if(datasize < maxBufSize)
    {
        uint16 bufleftlen =  Hal_VoiceBell_info.bufLen - Hal_VoiceBell_info.writedLen;
        uint16 needwirtelen = maxBufSize - datasize;

        if((bufleftlen == 0)&&(datasize == 0))
        {
            return VOICEBELL_SEND_COMPLETE;
        }

        HAL_ENTER_CRITICAL_SECTION(intState);

        if(needwirtelen < bufleftlen)
        {
            FillAudioBuffer(Hal_VoiceBell_info.pbuf + Hal_VoiceBell_info.writedLen, needwirtelen);
            Hal_VoiceBell_info.writedLen += needwirtelen;

        }
        else if(needwirtelen >= bufleftlen)
        {
            FillAudioBuffer(Hal_VoiceBell_info.pbuf + Hal_VoiceBell_info.writedLen, bufleftlen /*maxBufSize-datasize*/);
            Hal_VoiceBell_info.writedLen = Hal_VoiceBell_info.bufLen;
            //HAL_EXIT_CRITICAL_SECTION(intState);
            //return VOICEBELL_SEND_COMPLETE;
        }
        HAL_EXIT_CRITICAL_SECTION(intState);
    }


    return VOICEBELL_SEND_ING;
}


